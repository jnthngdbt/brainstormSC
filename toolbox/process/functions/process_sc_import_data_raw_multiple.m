%% ===== GET DESCRIPTION =====
function sProcess = GetDescription() %#ok<DEFNU>
    % Description the process
    sProcess.Comment     = 'IMPORT > Links to raw files';
    sProcess.FileTag     = '';
    sProcess.Category    = 'Custom';
    sProcess.Index       = sc_bst_process_index(25);
    sProcess.SubGroup    = sc_bst_process_subgroup(sProcess.Index);
    % Definition of the input accepted by this process
    sProcess.InputTypes  = {'import'};
    sProcess.OutputTypes = {'raw'};
    sProcess.nInputs     = 1;
    sProcess.nMinFiles   = 0;
    % Option: File to import
    sProcess.options.datafile.Comment = 'Files to import:';
    sProcess.options.datafile.Type    = 'datafile';
    sProcess.options.datafile.Value   = {...
        '', ...                                % Filename
        '', ...                                % FileFormat
        'open', ...                            % Dialog type: {open,save}
        'Open raw EEG/MEG recordings...', ...  % Window title
        'ImportData', ...                      % LastUsedDir: {ImportData,ImportChannel,ImportAnat,ExportChannel,ExportData,ExportAnat,ExportProtocol,ExportImage,ExportScript}
        'multiple', ...                        % Selection mode: {single,multiple}
        'files_and_dirs', ...                  % Selection mode: {files,dirs,files_and_dirs}
        bst_get('FileFilters', 'raw'), ...     % Get all the available file formats
        'DataIn'};                             % DefaultFormats
    % Separator
    sProcess.options.separator.Type = 'separator';
    sProcess.options.separator.Comment = ' ';
    % Align sensors
    sProcess.options.channelalign.Comment = 'Align sensors using headpoints';
    sProcess.options.channelalign.Type    = 'checkbox';
    sProcess.options.channelalign.Value   = 0;
end


%% ===== FORMAT COMMENT =====
function Comment = FormatComment(sProcess) %#ok<DEFNU>
    Comment = sProcess.Comment;
end


%% ===== RUN =====
function OutputFiles = Run(sProcess, sInputs) %#ok<DEFNU>
    OutputFiles = {};
    
    datafiles = sProcess.options.datafile.Value{1};
    format = sProcess.options.datafile.Value{2};
    Nf = numel(datafiles);
    for i=1:Nf

        % Script generated by Brainstorm v3.1 (11-Jun-2013)

        % Input files
        FileNamesA = [];

        [p,subjectname,x] = fileparts(datafiles{i});
        % Process: Create link to raw file
        sFiles = bst_process(...
            'CallProcess', 'process_import_data_raw', ...
            FileNamesA, [], ...
            'subjectname', subjectname, ...
            'datafile', {datafiles{i}, format}, ...
            'channelalign', sProcess.options.channelalign.Value);

        
        OutputFiles{i} = sFiles.FileName;
    %     bst_report('Open', ReportFile);
    end
    
end



